
Ratings = float(input("Enter Movie Rating out of 5: "))

if Ratings >= 4.5:
    print("Movie is BLOCKBUSTER")
elif Ratings >= 3.5:
    print("Movie is Hit")
elif Ratings >= 3:
    print("Movie is Good")
elif Ratings >= 2:
    print("Movie is Average")
elif Ratings >= 1.5:
    print("Movie is Below Average")
else:
    print("Movie is Flop")

    


